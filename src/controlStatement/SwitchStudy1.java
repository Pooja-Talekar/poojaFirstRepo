package controlStatement;

public class SwitchStudy1 
{

	public static void main(String[] args)
	{
		//Navratri Colors 
		//1.White
		//2.Red
		//3.Blue
		//4.Green
		//5.Grey
		//6.Orange
		
		
		String color = "Black";
		
		switch(color)
		{
		case "White" :System.out.println("Today is White color day");
		break;
		
		case "Red" :System.out.println("Today is Red color day");
		break;
		
		case "Blue" : System.out.println("Today is Blue color day");
		break;
		
		case "Green" : System.out.println("Today is Green color day");
		break;
		
		case "Grey" : System.out.println("Today is Grey color day");
		break;
		
		case "Orange" : System.out.println("Today is Orange color day");
		break;
		
		default : System.out.println("Please enter Navaratri colors for 2022");
		}
		

	}

}
