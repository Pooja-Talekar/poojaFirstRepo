package Casting;

public class ExplicitCasting 
{

	public static void main(String[] args) 
	{
		//Explicit Casting or Narrowing casting
		//Higher to higher
		//Size: decreases
		//Memory: Data may loss
				
		double a=10.35426;
		System.out.println(a);
				
		int b=(int)a;
		System.out.println(b);

	}

}
