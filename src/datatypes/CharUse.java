package datatypes;

public class CharUse {

	public static void main(String[] args)
	{
		// I want to store gender
		
		//1. Variable declaration 	
		
		char gender;
		
		//2.variable initialization 
		gender = 'F';//for char data type every time use single quote('')
		
		//3.Usage
		
		System.out.println("My gender is : " +gender);
		
		//I want to store my grade --> A,B,C,D
		
		char grade;
		
		grade='B';
		
		System.out.println("Sachin grade is : " +grade);

	}

}
